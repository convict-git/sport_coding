signed main() {
  IOS; PREC;

  string s, t;
  int T = 0;
  while (cin >> s >> t) {
    if (T) cout << '\n';
    ++T;
    int n = (int)s.size(), m = (int)t.size();

    int dp[n + 1][m + 1];
    memset(dp, 0, sizeof(dp));

    int res = 0;
    for (int i = n - 1; i >= 0; --i) {
      for (int j = m - 1; j >= 0; --j) {
        if (s[i] == t[j]) dp[i][j] = 1 + dp[i + 1][j + 1];
        res = max(res, dp[i][j]);
      }
    }

    vector <pair <int, int>> mx;
    for (int i = 0; i < n; ++i)
      for (int j = 0; j < m; ++j)
        if (dp[i][j] == res) mx.push_back(make_pair(i, j));

    if (res == 0) cout << "No common sequence.\n";
    else {
      set <string> st;
      for (auto el : mx) {
        string foo = "";
        int i_ = el.first, j_  = el.second;
        while (i_ < n && j_ < m && s[i_] == t[j_]) foo += s[i_], ++i_, ++j_;
        if (foo.size()) st.insert(foo);
      }
      for (auto el : st) cout << el << '\n';
    }
  }

  return EXIT_SUCCESS;
}


