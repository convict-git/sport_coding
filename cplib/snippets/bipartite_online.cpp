const int N = (int)1e5 + 10;
int rnk[N], bipartite[N], pi[N], parity[N];

int make_set (int v) {
  rnk[v] = 0, pi[v] = v, parity[v] = 0, bipartite[v] = true;
}

int find_set (int v) {
  if (v != pi[v]) {
    int x = parity[v], pr = pi[v];
    pi[v] = find_set(pi[v]);
    parity[v] = x ^ parity[pr];
  }
  return pi[v];
}

bool is_bipartite (int v) {
  return bipartite[find_set(v)];
}

void union_edge (int u, int v) {
  int ru = find_set(u), rv = find_set(v);
  int pu = parity[u], pv = parity[v];
  if (ru == rv) { // already in same component
    if (pu == pv) { // odd length cycle
      bipartite[ru] = false; // this component is no more bipartite
    }
    else {
      // nothing to do (already set up the way we want)
    }
  }
  else { // in different componet
    // ru is prefered
    if (rnk[ru] < rnk[rv]) {
      swap(ru, rv);
    }
    pi[rv] = ru;
    parity[rv] = pu ^ pv ^ 1;
    bipartite[ru] &= bipartite[rv]; // finally is this component bipartite
    if (rnk[ru] == rnk[rv])
      ++rnk[ru];
  }
}

